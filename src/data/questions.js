export function computeQuadrantScores(checked) {
  const scores = {};
  sections.forEach((section) => {
    section.quadrants.forEach((quad) => {
      const count = quad.questions.filter(
        (_, i) => checked[`q${quad.globalStart + i}`]
      ).length;
      scores[`${section.id}-${quad.id}`] = { count, total: quad.questions.length };
    });
  });
  return scores;
}
